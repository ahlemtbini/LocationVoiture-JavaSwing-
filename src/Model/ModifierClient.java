    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String pid =txtpermis.getSelectedItem().toString();
        try{
        pst = (PreparedStatement) con.prepareStatement("select * from client where NumPermis=?");
        pst.setString(1, pid);
        rs = pst.executeQuery();
        if(rs.next()==true)
        {
            txtnom.setText(rs.getString(2));
            txtprenom.setText(rs.getString(3));
            txtadress.setText(rs.getString(4));
            txttel.setText(rs.getString(5));

        }
        }catch(Exception e){
         e.printStackTrace();
        }
        Loadnumpermis();

    }//GEN-LAST:event_jButton2ActionPerformed

