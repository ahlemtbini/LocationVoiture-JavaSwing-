    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         try{
    Connecter();
    String pmarque =txtmarque.getText();
    String pmodele =txtmodele.getText();
    String pnbplaces =txtnbrplaces.getText();
    String pcouleur =txtcouleur.getText();
    String pid =txtmatricule.getSelectedItem().toString();

    pst =(PreparedStatement) con.prepareStatement("UPDATE vehicule SET marque=? ,modele =? ,nbrPlaces =? ,couleur =? WHERE matricule =?");
    pst.setString(1, pmarque);
    pst.setString(2, pmodele);
    pst.setString(3, pnbplaces);
    pst.setString(4, pcouleur);
    pst.setString(5, pid);

    pst.executeUpdate();
    con.close();
   
    JOptionPane.showMessageDialog(null, "vehicule modifier");
    Loadmatricule();

}catch(Exception e)
{
e.printStackTrace();
}
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String pid =txtmatricule.getSelectedItem().toString();
        try{
        pst = (PreparedStatement) con.prepareStatement("select * from vehicule where matricule=?");
        pst.setString(1, pid);
        rs = pst.executeQuery();
        if(rs.next()==true)
        {
            txtmarque.setText(rs.getString(2));
            txtmodele.setText(rs.getString(3));
            txtnbrplaces.setText(rs.getString(4));
            txtcouleur.setText(rs.getString(5));

        }
        }catch(Exception e){
         e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

