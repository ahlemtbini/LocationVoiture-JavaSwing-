    public void Loadmatricule(){
        try{
        pst = (PreparedStatement) con.prepareStatement("select matricule from vehicule");
        rs = pst.executeQuery();
        txtmatricule.removeAllItems();
        while(rs.next())
        {
            txtmatricule.addItem(rs.getString(1));
        }
        }catch(Exception e){
            e.printStackTrace();
        }
    }

